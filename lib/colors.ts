export const getColorValue = (colorName: string): string | null => {
  const normalized = colorName.toLowerCase().trim();
  return colorMap[normalized] || null;
};

export const isColorAttribute = (attributeName: string): boolean => {
  const normalized = attributeName.toLowerCase();
  return normalized.includes('couleur') || normalized.includes('color');
};
